package nestedifelse;

public class elseifnested {
	
	public static void main (String[] args) {
		
	int day=2;
	
	if (day==1)
	{
		System.out.println("monday");
	}
	else if (day==2)
	{
		System.out.println("tuesday");
		
	}
	else if (day==3)
	{
		System.out.println("wednesday");
	}
		
		
		
		
		
	}
	

}
